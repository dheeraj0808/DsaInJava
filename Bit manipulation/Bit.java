public class Bit{
    public static void main(String[] args) {
        System.out.println(5|6);
        System.out.println(5&6);
        //xor operator
        System.out.println(5^6);
        //pahle bit me solve karta hai uske baad se phir se wo bit me convert karta hai 
        //one's operator or one's complement
        System.out.println(~5);
        //left shift
        System.out.println(5<<2);
        //right shift
        System.out.println(6>>1);
    }
}